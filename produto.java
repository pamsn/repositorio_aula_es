public class Produto {

	private String nome;
	private double preco;
	private int quantidade;
	private String marca;
	private String tam;

	public Produto(String nome, double preco, int quantidade, String marca, String tam){
		this.nome = nome;
		this.preco = preco;
		this.quantidade = quantidade;
		this.marca = marca;
		this.tam = tam;
	}
}




