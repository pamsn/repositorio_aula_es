public class Produto {

	private String nome;
	private double preco;
	public int qtd;
	private String marca;
	private String tamanho;

	public Produto(String nome, double preco, int qtd, String marca, String tamanho){
		this.nome = nome;
		this.preco = preco;
		this.qtd = qtd;
		this.marca = marca;
		this.tamanho = tamanho;
	}
}




